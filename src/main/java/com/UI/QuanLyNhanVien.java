    /* setForm cho Loại phòng */
    public void setForm(NhanVien nv) {
        txtManv.setText(nv.getMaNV());
        txthoTen.setText(nv.getHoTen());
        txtnamSinh.setDate(nv.getNamSinh());
        txtdiaChi.setText(nv.getDiaChi());
        txtSDT.setText(nv.getSoDT());
        rdo_NhanVien.setSelected(nv.getChuVu());
        rdo_QuanLy.setSelected(!nv.getChuVu());
        rdoNam.setSelected(nv.getGioiTinh());
        rdoNu.setSelected(!nv.getGioiTinh());
        txteMail.setText(nv.getEmail());
        txtmatKhau.setText(nv.getMaKhau());
    }

    /* getForm cho Loại phòng */
    public NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtManv.getText());
        nv.setHoTen(txthoTen.getText());
        nv.setNamSinh(txtnamSinh.getDate());
        nv.setDiaChi(txtdiaChi.getText());
        nv.setSoDT(txtSDT.getText());

        boolean cv;
        if (rdo_NhanVien.isSelected()) {
            cv = true;
        } else {
            cv = false;
        }
        nv.setChuVu(cv);

        boolean gt;
        if (rdoNam.isSelected()) {
            gt = true;
        } else {
            gt = false;
        }
        nv.setGioiTinh(gt);
        nv.setEmail(txteMail.getText());
        nv.setMaKhau(new String(txtmatKhau.getPassword()));

        return nv;
    }

    public void insert() {

        NhanVien nv = getForm();

        if (txtManv.getText().equals("")) {
            MsgBox.warring(this, "Mã nhân viên không được để trống!");
            txtManv.requestFocus();
            txtManv.setBackground(Color.yellow);
            return;
        } else {
            txtManv.setBackground(Color.white);
        }
        if (txthoTen.getText().equals("")) {
            MsgBox.warring(this, "Họ và tên không được để trống!");;
            txthoTen.requestFocus();
            txthoTen.setBackground(Color.yellow);
            return;
        } else {
            txthoTen.setBackground(Color.white);
        }
        if (txtdiaChi.getText().equals("")) {
            MsgBox.warring(this, "Địa chỉ không được để trống!");
            txtdiaChi.requestFocus();
            txtdiaChi.setBackground(Color.yellow);
            return;
        } else {
            txtdiaChi.setBackground(Color.white);
        }
        if (txtSDT.getText().equals("")) {
            MsgBox.warring(this, "Số ĐT không được để trống");
            txtSDT.requestFocus();
            txtSDT.setBackground(Color.yellow);
            return;
        } else {
            txtSDT.setBackground(Color.white);
        }
        String phone = "0\\d{9}";
        if (!txtSDT.getText().matches(phone)) {
            MsgBox.warring(this, "Số ĐT không đúng định dạng");
            txtSDT.requestFocus();
            txtSDT.setBackground(Color.yellow);
            return;
        } else {
            txtSDT.setBackground(Color.white);
        }
        if (txteMail.getText().equals("")) {
            MsgBox.warring(this, "Email không được để trống!");;
            txteMail.requestFocus();
            txteMail.setBackground(Color.yellow);
            return;
        } else {
            txteMail.setBackground(Color.white);
        }
//        st.Email = txtEmail.getText();
        String Email = "\\w+@\\w+(\\.\\w+){1,2}";
        if (!txteMail.getText().matches(Email)) {
            MsgBox.warring(this, "Email không đúng định dạng!");
            txteMail.requestFocus();
            txteMail.setBackground(Color.yellow);
            return;
        } else {
            txteMail.setBackground(Color.white);
        }
        if (txtmatKhau.getText().equals("")) {
            MsgBox.warring(this, "Mật khẩu không được để trống");
            txtmatKhau.requestFocus();
            txtmatKhau.setBackground(Color.yellow);
            return;
        } else {
            txtmatKhau.setBackground(Color.white);

        }

        try {

            dao.insert(nv);
            this.fillToTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm thành công!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

