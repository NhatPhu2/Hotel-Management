    private void edit() {
        String maNV = (String) tbl_NhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(maNV);
        this.setForm(nv);
        this.updateStatus();
    }

    public void updateStatus() {
        boolean editRoom = (this.row >= 0);
        boolean firstRoom = (this.row == 0);
        boolean lastRoom = (this.row == tbl_NhanVien.getRowCount() - 1);

        btn_ThemNV.setEnabled(editRoom);
        btn_SuaNV.setEnabled(editRoom);
        btn_XoaNV2.setEnabled(editRoom);

    }

    public void first() {
        this.row = 0;
        this.edit();
    }

    public void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    public void next() {
        if (this.row < tbl_NhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    public void last() {
        this.row = tbl_NhanVien.getRowCount() - 1;
        this.edit();
    }

    private void clearForm() {
        NhanVien nv = new NhanVien();
        settext();
        this.row = -1;
        this.updateStatus();
    }

