    void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_NhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                model.addRow(new Object[]{
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getSoDT(),
                    nv.getDiaChi(),
                    nv.getEmail(),
                    nv.getChuVu() ? "Quản Lý" : "Nhân Viên",
                    nv.getNamSinh(),
                    nv.getMaKhau(),
                    nv.getGioiTinh() ? "Nam" : "Nữ",});
            }

        } catch (Exception e) {

        }
    }

