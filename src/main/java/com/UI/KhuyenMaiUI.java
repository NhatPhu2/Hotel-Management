  KhuyenMaiDAO dao = new KhuyenMaiDAO();
    int row = -1;

    private void init() {
        this.setLocationRelativeTo(null);
        setTable();
        filltable();
        chon();
    }

    private void setTable() {
        JTableHeader hd2 = tbl_khuyenMai.getTableHeader();
        hd2.setBackground(new Color(0, 102, 255));
        hd2.setForeground(Color.white);
        tbl_khuyenMai.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 16));
        tbl_khuyenMai.setShowHorizontalLines(false);
        tbl_khuyenMai.setShowVerticalLines(false);
    }

    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tbl_khuyenMai.getModel();
        model.setRowCount(0);
        try {
            List<KhuyenMai> list = dao.selectAll();
            for (KhuyenMai kM : list) {
                Object[] row = {kM.getMaKM(), kM.getTenKM(), kM.getNoiDung(), kM.getNgayBD(), kM.getNgayKT(), kM.getPhanTramGiam()};
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    public boolean checkForm(){
        if(txt_MaKM.getText().trim().isEmpty()){
            MsgBox.alert(null,"Không để trống mã khuyến mãi");
            return false;
        }else if(txt_NoiDung.getText().trim().isEmpty()){
             MsgBox.alert(null,"Không để trống nội dung");
            return false;
        }else if(txt_TenKM.getText().trim().isEmpty()){
             MsgBox.alert(null,"Không để trống tên khuyến mãi");
            return false;
        }else if(txtgiamGia.getText().trim().isEmpty()){
             MsgBox.alert(null,"Không để trống giảm giá");
            return false;
        }else if(txtngayBD1.getDate() == null){
             MsgBox.alert(null,"Không để trống ngày bắt đầu");
            return false;
        }else if(txtngayKetThuc1.getDate() == null){
             MsgBox.alert(null,"Không để trống ngày kết thúc");
            return false;
        }
        try{
            float f = Float.parseFloat(txtgiamGia.getText());
        }catch(NumberFormatException e){
            MsgBox.alert(null,"Phần trăm giảm không hợp lệ(không nhập ký tự đặc biệt)");
            return false;
        }
        return true;
    }

    KhuyenMai getForm() {
        KhuyenMai kM = new KhuyenMai();
        kM.setMaKM(txt_MaKM.getText());
        kM.setTenKM(txt_TenKM.getText());
        kM.setNoiDung(txt_NoiDung.getText());
        kM.setNgayBD(txtngayBD1.getDate());
        kM.setNgayKT(txtngayKetThuc1.getDate());
        kM.setPhanTramGiam(Float.parseFloat(txtgiamGia.getText()));
        return kM;
    }

    void setform(KhuyenMai kM) {
        txt_MaKM.setText(kM.getMaKM());
        txt_TenKM.setText(kM.getTenKM());
        txt_NoiDung.setText(kM.getNoiDung());
        txtngayBD1.setDate(txtngayBD1.getDate());
        txtngayKetThuc1.setDate(txtngayKetThuc1.getDate());
        txtgiamGia.setText(String.valueOf(kM.getPhanTramGiam()));
    }

    void insert() {
        if(checkForm() == false)
            return;
        KhuyenMai kM = getForm();
       
            dao.insert(kM);
            this.filltable();
            this.cleanForm();
            MsgBox.alert(null,"Thêm thành công");
        

    }

    void cleanForm() {
        KhuyenMai kM = new KhuyenMai();
        this.setform(kM);
        this.row = 0;
    }

    void update() {
        if(checkForm() == false)
            return;
        KhuyenMai kM = getForm();
        
            dao.update(kM);
            this.filltable();
            this.cleanForm();
            MsgBox.alert(null,"Cập nhật thành công");
        
    }

    void chon() {
        tbl_khuyenMai.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (tbl_khuyenMai.getSelectedRow() >= 0) {
                    txt_MaKM.setText(tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 0) + "");
                    txt_TenKM.setText(tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 1) + "");
                    txt_NoiDung.setText(tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 2) + "");
                    txtngayBD1.setDate((Date) tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 3));
                    txtngayKetThuc1.setDate((Date) tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 4));
                    txtgiamGia.setText(tbl_khuyenMai.getValueAt(tbl_khuyenMai.getSelectedRow(), 5) + "");
                }
            }
        });
    }

    void delete() {
        String maKM = txt_MaKM.getText();
        try {
            dao.delete(maKM);
            this.filltable();
            this.cleanForm();
             MsgBox.alert(null,"Xóa thành công");

        } catch (Exception e) {
            e.printStackTrace();
           
        }

    }
