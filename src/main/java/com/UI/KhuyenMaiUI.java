    private javax.swing.JTextField txt_NoiDung;
    private javax.swing.JTextField txt_TenKM;
    private javax.swing.JTextField txtgiamGia;
    private com.toedter.calendar.JDateChooser txtngayBD1;
    private com.toedter.calendar.JDateChooser txtngayKetThuc1;
